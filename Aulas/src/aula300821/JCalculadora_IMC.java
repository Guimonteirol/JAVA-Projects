       int topo = 0;
       double soma = 0;
       
    public String calcularimc(){
           String resp = " ";
           double peso = Double.parseDouble(txtpeso.getText());
           double altura = Double.parseDouble(txtaltura.getText());

           for(int i = 0; i<imc.length;i++){     
               if(imc[i] == 0){
                     imc[i] = peso/(altura*altura);
                     soma = imc[i] + soma;
                     topo++;
                     break;
               }
            }
            for(int i = 0; i<topo; i++){
                resp = resp + " " + imc[i];
            }
           return resp;
       }
    
    public String somaimc(){
        String resp = " ";
        resp = resp +" "+ soma;
        
        return resp;
    }
    
    public String mediaimc(){
        String resp = " ";
           double media;
           media = soma / topo;
           resp = resp + " " + media;
           
        return resp;
    }
    
    public String imcideal(){
         String resp = " ";
         double imci = 0;
         for(int i = 0; i<topo; i++){
             if(imc[i]>18.4 && imc[i]< 25){
                 imci = imc[i];
                 resp = resp + " "+ imci;
             }     
         }
         
        return resp;
    }
    public String imcmorbida(){
        String resp = " ";
        double imcb = 0;
        for(int i = 0; i<topo; i++){
            if(imc[i]>=40){
                imcb = imc[i];
                resp = resp + " "+ imcb;
            }
        }
         return resp;
    }
    public String imcabaixo(){
        String resp = " ";
        double imca = 0;
        for(int i =0; i<topo;i++){
            
        if(imc[i]<18.5){
        imca = imc[i];
        resp = resp + " " + imc[i];
        }
    }
        
        return resp;
    }
    public String exibirimc(){
        String resp = " ";
        for(int i = 0; i<topo; i++){
            resp = resp + " " + imc[i];
        }
        return resp;
    }
    
