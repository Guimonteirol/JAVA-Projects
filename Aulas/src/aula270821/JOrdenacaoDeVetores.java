       public String crescente(){
         String respo ="";
         for(int i = 0; i<topo; i++){
             respo = respo + " " + valores[i]; 
         } 
         
         return respo;
       }
       
       public String decrescente(){
           String res = "";
           int maior;
           int posicao;
           boolean troca;
           int aux;
           for(int i =0; i<topo; i++){
               maior = valores[i];
               posicao = i;
               troca = false;
               for(int j = i+1; j<topo; j++){
                   if(maior < valores[j]){
                       maior = valores[j];
                       posicao = j;
                       troca = true;
                   }                  
               }   
               if(troca == true){
                       aux = valores[i];
                       valores[posicao] = aux;
                       valores[i] = maior;
                   }
           }
         for(int i = 0; i<topo; i++){
                   res = res + " " + valores[i];
               }
           return res;
       }
