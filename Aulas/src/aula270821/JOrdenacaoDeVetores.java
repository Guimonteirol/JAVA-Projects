    int valores []= new int[10]; /*DECLAREI O VETOR*/
       String resp = " ";/*STRING PARA DEFINIR A RESPOSTA*/
       int topo;/*TOPO - VARIÁVEL QUE VAI AUX. NA LEITURA DO VETOR, FAZENDO C Q LEIA ATÉ ONDE FOI USADO*/
       
       public String exibirelementos(){ /*UM MÉTODO PARA EXIBIR O ELEMENTO*/
           resp = "";
           for(int i = 0; i<topo; i++){
               resp = resp + " " + valores[i];
           }
           return resp;
       }
       
       public void Limpar(){ /*CRIEI UM MÉTODO LIMPAR*/
           txtv.setText("");       
           txtv.requestFocus();
       }
       
       public void addElementos(int valor){ /*UM MÉTODO PARA ADICIONAR OS ELEMENTOS*/
            for(int i = 0; i<valores.length; i++){
           if(valores[i]== 0){
            valores[i] = valor;
            //resp = resp + " " + valores[i];
            topo++;
          
            break;
        }            
       }
       }
       
       public boolean removerelemento(int valor){ /*AQUI EU VOU ESCOLHER UM ELEMENTO E EXCLUIR DO MEU VETOR*/
           boolean resp = false;
           for(int i= 0; i<topo; i++){
               if(valores[i] == valor){
                    valores[i] = 0;
                    resp = true;
                    topo--;  /*AQUI EU CONSIGO TIRAR O ELEMENTO 0*/
                    break;
               }
           }
           return resp;
       }
       
       public String maiorelemento(){
          int maior = -999;
           resp="";
           for(int i = 0; i<topo; i++){
               if(valores[i] > maior){
                   maior = valores[i];
                   resp = "O maior elemento é: " + maior;
               }
           }
           return resp;
       }
       
       public String menorelemento(){
           int menor = +999;
           resp="";
           for(int i = 0; i<topo; i++){
               if(valores[i] < menor){
                   menor = valores[i];
                   resp = "O menor valor é: " + menor;
               }
           }
         return resp;  
       }
       
